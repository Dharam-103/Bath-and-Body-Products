let box=document.querySelector("#basic")
function showData(items){
    box.innerHTML=null;
    items.map(function(el){
      
    let div=document.createElement("div")
    let images=document.createElement("img")
    images.setAttribute("src",el.images)
    let titles=document.createElement("h3")
    titles.innerText=el.titles
    let sidetitles=document.createElement("p")
    sidetitles.innerText=el.sidetitles
    sidetitles.style.fontSize="13px";
    let prices=document.createElement("h5")
    prices.innerText=`prices: $${el.prices}`
    let rating=document.createElement("h5")
    rating.innerText=`Rating: ${el.rating}`
  
    let button=document.createElement("button")
    button.innerText="Add To Bag"
    button.id="btn"
    button.style.background="black";
    button.style.color="white";
    
  
  div.append(images,titles,sidetitles,prices,rating,button)
  box.append(div)
  
  })

}

